public class Employee {

	public String firstName;
	public String lastName;
	public int monthSalary;
	public double monthSalaryRaise;
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public int getMonthSalary() {
		return monthSalary;
	}
	public void setMonthSalary(int monthSalary) {
		this.monthSalary = monthSalary;
	}
	public double getMonthSalaryRaise() {
		return monthSalaryRaise;
	}
	public void setMonthSalaryRaise(double monthSalaryRaise) {
		this.monthSalaryRaise = monthSalaryRaise;
	}

}